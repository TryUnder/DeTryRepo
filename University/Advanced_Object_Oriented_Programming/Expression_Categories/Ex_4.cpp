#include <iostream>

int main() {
    int x = 1, y = 2;

    //Przykład konwersji
    x + y;
    /*
    l-wartość, która nie jest ani funkcją, ani typem T, może zostać przekonwertowana do r-wartości.
    operator "+" na intach, oczekuje r-wartości jako swoich operandów. W powyższym przykładzie,
    x i y są przekonwertowane na r-wartości. 
    */
}